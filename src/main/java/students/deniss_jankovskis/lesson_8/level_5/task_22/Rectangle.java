package students.deniss_jankovskis.lesson_8.level_5.task_22;

class Rectangle {

    public double height, width;

    Rectangle(double height, double width) {
        this.height = height;
        this.width = width;
    }

    @Override
    public String toString() {
        return "Rectangle{" +
                "height = " + height +
                ", width = " + width +
                '}';
    }

}
