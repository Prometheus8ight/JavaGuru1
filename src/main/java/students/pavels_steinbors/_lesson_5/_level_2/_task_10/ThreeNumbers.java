class ThreeNumbers {

    public static void main(String[] args) {

        int[] number = new int[3];
        number[0] = 1;
        number[1] = 2;
        number[2] = 3;

        System.out.println("number 1 = " + number[0]);
        System.out.println("number 2 = " + number[1]);
        System.out.println("number 3 = " + number[2]);
    }
}
